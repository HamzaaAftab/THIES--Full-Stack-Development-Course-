const mongoose = require('mongoose');

// Define Course schema
const courseSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Export Course model
module.exports = mongoose.model('Course', courseSchema);
